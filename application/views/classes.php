<h3>Virtudes</h3>

<p><strong>Alfabetizado</strong>: Possui a capacidade de ler e redigir documentos nas línguas que domina.</p>
<p><strong>Preceito</strong>: Sempre que seguir um dos preceitos do panteão, realiza um teste de karma. Se for
    bem-sucedido, o ato fortalece sua fé, concedendo 1 ponto de moral.</p>
<p><strong>Agraciado</strong>: Tem a habilidade de rogar aos deuses por bênçãos e graças.</p>
<p><strong>Missão</strong>: Pode utilizar sua moral como atributo ao atacar criaturas ereboreanas, mortos-vivos ou seres
    amaldiçoados.</p>
<p><strong>Caminho</strong>: Deve escolher uma divindade a quem o arauto serve com maior devoção. Ao pedir graças dessa
    divindade, recebe um bônus de +3 no teste de moral e -1 no consumo de moral, sendo o custo sempre de ao menos 1
    ponto.</p>
<p><strong>Acolhimento</strong>: Arautos são geralmente bem recebidos em suas jornadas. Por isso, é fácil encontrar
    abrigo e alimento em locais onde haja comunidades humanas.</p>

<h2>Ocultista</h2>

<p>Desde os primórdios da humanidade e das primeiras civilizações, as tribos recorriam à figura do oráculo em busca de
    sabedoria. Os oráculos eram pessoas dotadas de um poder incomum: a capacidade de se comunicar com o mundo
    espiritual, dos domínios de Erebos, interagindo até mesmo com os demônios que habitam esse plano.</p>
<p>Com o avanço da civilização humana em Avengard, o conhecimento sobre as verdadeiras divindades trouxe uma reviravolta
    para os antigos oráculos. O dom que antes era visto como uma bênção passou a ser considerado sombrio e malicioso por
    grande parte da sociedade, o que levou essas figuras a serem chamadas de ocultistas. Com o tempo, o ocultismo foi
    proibido em várias cidades, e seus praticantes passaram a esconder suas habilidades por medo de perseguição.</p>
<p>O ocultista, no entanto, mantém uma conexão inata com um dos domínios de Erebos, o local para onde os humanos vão
    após a morte. Por meio dessa interação, eles conseguem se comunicar com os mortos e até pedir favores a demônios
    dispostos a cooperar.</p>
<p><strong>Equipamento Inicial</strong>: Kit de viajante e 2 moedas de prata.</p>
<p><strong>Ganho de Atributo</strong>: +2 em karma, +1 em sabedoria, -1 em constituição.</p>
<h3>Virtudes</h3>
<p><strong>Poder Oculto</strong>: O ocultista possui uma estranha conexão com o Miska, que lhe permite acessar o mundo
    dos mortos em diferentes planos de existência. Isso o torna o único capaz de utilizar os talentos do ocultismo.</p>
<p><strong>Guarda Espiritual</strong>: Um guia espiritual, semelhante a um anjo da guarda, acompanha o ocultista em
    silêncio. Em momentos de perigo iminente, ele pode alertá-lo, e durante o combate, oferece +1 em sua defesa.</p>
<p><strong>Detectar Intenções</strong>: O ocultista consegue ver a cor da aura das pessoas e criaturas, revelando se
    elas estão amigáveis, hostis ou neutras. Essa percepção não reflete a relação direta com o ocultista, mas sim o que
    domina os pensamentos do alvo no momento.</p>
<p><strong>Ouvir Vozes</strong>: O ocultista escuta com frequência vozes provenientes de outros planos. Essas vozes
    podem ser amistosas, hostis, trazer avisos ou até informações falsas, oriundas de pequenos demônios zombeteiros.
    Cabe ao narrador explorar esse recurso de diversas formas, podendo até determinar por sorteio que tipo de voz será
    ouvida, sem que o jogador saiba sua real natureza.</p>
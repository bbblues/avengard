<h2>Arauto</h2>

<p>Alguns indivíduos encontram sua vocação por meio de sua fé, uma crença inabalável no panteão que os conecta
    profundamente ao divino. Por intermédio de um arauto, um deus pode manifestar sua vontade sem a necessidade de
    presença física. Os escolhidos por essas divindades recebem bênçãos de diversas formas, em troca de sua fé e
    devoção.</p>
<p>O arauto vive segundo os preceitos sagrados e quebrá-los pode acarretar graves consequências. Seu principal preceito
    é o respeito à vida humana, pois aqueles que outrora foram seres apócrifos agora são consagrados pelas divindades.
    Por isso, é raro encontrar um arauto que não seja justo e benevolente. Se houver, é provável que esteja em declínio
    espiritual, perdendo sua fé. A vida do arauto é geralmente dedicada ao serviço ao próximo, e suas ações têm como
    objetivo o bem-estar coletivo.</p>
<p>Para o arauto, sua moral é inseparável de sua fé. Seguir os preceitos fortalece sua moral, enquanto agir em oposição
    a eles traz desonra. Assim, aqueles que têm sua fé consolidada são também altamente confiantes e determinados. No
    entanto, pedir uma graça a um deus é visto como um sinal de fraqueza ou insuficiência, abalando sua fé. Cada súplica
    realizada, portanto, enfraquece sua moral.</p>
<p>É raro ver um arauto empunhando armas cortantes ou perfurantes, já que elas têm maior potencial para tirar vidas
    rapidamente, e preservar a vida é sua principal preocupação. Para o arauto, o combate é apenas uma forma de
    autodefesa, sempre que possível evitando mortes. Se tiver de desferir o golpe final contra um humano ou criatura
    avengardiana, ele optará por incapacitar ao invés de matar. Contudo, essa mesma consideração não se aplica a
    criaturas ereboreanas, mortos-vivos, ou seres amaldiçoados; nestes casos, pôr fim à existência é visto como um
    dever.</p>

<p><b>Equipamento Inicial:</b> &nbsp;O arauto inicia a jornada com um kit viajante, 1 moeda de prata, uma arma de
    contusão, e um escudo em caso de arma de uma mão.</p>

<p><b>Ganho de atributo:</b> recebe +1 em sabedoria, constituição e karma, e -1 e astúcia.</p>


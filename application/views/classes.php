<h3>Virtudes</h3>

<p><strong>Alfabetizado</strong>: O Corsário sabe ler e escrever nas línguas que domina.</p>
<p><strong>Esgrimista Nato</strong>: o dado de dano do sabre é dobrado, jogando-se duas vezes.</p>
<p><strong>Navegação</strong>: Ele é um mestre em navegação, incluindo nadar, cartografia, geolocalização e previsão do
    clima. Sempre faz testes relacionados a esses temas com vantagem.</p>
<p><strong>Persuasivo</strong>: O Corsário é um exímio orador, capaz de convencer, mentir, fingir, falar em público ou
    negociar com facilidade. Testes nestes sentidos são feitos com vantagem.</p>
<p><strong>Sorte</strong>: O universo parece conspirar a favor do Corsário. Sempre que realizar um teste de sorte
    (karma), pode fazê-lo com vantagem.</p>
<p><strong>Em Guarda</strong>: Enquanto estiver em posição de guarda, o Corsário não pode ser flanqueado,
    movimentando-se com astúcia.</p>
<p><strong>Touché!</strong>: Ao usar um sabre, se o valor do d20 for inferior à guarda do adversário, o Corsário pode
    desferir um novo ataque, seguindo a mesma regra. Essa habilidade só pode ser usada quando não estiver em guarda.</p>
<p><strong>Esquiva Inteligente</strong>: O Corsário pode usar seu valor de astúcia no lugar da destreza para calcular
    sua guarda e a destreza máxima da armadura.</p>
<p><strong>Estratagema</strong>: Sempre que acertar um <strong>Touché!</strong>, o Corsário ganha um ponto de
    estratagema, que pode ser acumulado e gasto até o final do combate. Um ponto de estratagema permite ao Corsário
    fazer uma intervenção narrativa no combate, realizando um movimento engenhoso, como saltar de um candelabro ou
    derrubar uma pilha de barris sobre o inimigo, mesmo que esses elementos não tenham sido previamente descritos pelo
    narrador. Esses movimentos seguem as regras da narrativa compartilhada, sem consequências em caso de falha.</p>

<h2>Ladino</h2>

<p>O Ladino é uma classe que prospera nas movimentadas e superpovoadas cidades de Avengard, onde a densa concentração de
    pessoas trouxe um efeito colateral indesejado: um alto índice de criminalidade. Nesse cenário caótico, atividades
    ilícitas se espalham como uma epidemia, e é nesse ambiente que surgem os ladrões e assassinos profissionais,
    especialistas em agir nas sombras e eliminar alvos sem deixar vestígios.</p>
<p>A maioria dos ladinos mantém suas identidades ocultas, compreendendo que sua sobrevivência depende do anonimato.
    Tanto governantes quanto o misterioso Sindicato contratam seus serviços para operações clandestinas. Alguns mais
    famosos adotam alguma alcunha, buscando melhores contratos.</p>
<p>A força dos ladinos está em sua destreza e furtividade, o que também os torna excelentes ladrões. Mestres em eliminar
    alvos sem se expor ao perigo, frequentemente fazem uso de venenos aplicados por dardos, adagas ou zarabatanas. Além
    disso, utilizam uma série de ferramentas que facilitam sua fuga ou lhes dão vantagem em combate, como bombas de
    fumaça.</p>
<p><strong>Equipamento Inicial</strong>: O ladino pode começar com um sabre ou duas adagas, além de uma zarabatana. Ele
    carrega um kit de fabricação de venenos e antídotos, junto de seu kit de viajante. Sempre possui algo para cobrir o
    rosto, como uma máscara ou capuz, e um kit de arrombamento. Inicia sem armadura e com 5 moedas de prata.</p>
<p><strong>Ganho de Atributos</strong>: A destreza do ladino é aumentada em +2, a astúcia em +1, e sua força é reduzida
    em -1.</p>

<!-- <img src="<?=base_url('assets/img/ladino.png')?>" alt=""> -->

<h3>Virtudes</h3>

<p><strong>Técnica Refinada</strong>: Qualquer arma que utilize a destreza como base causará o dobro dos dados de dano.
    Joga-se duas vezes os dados de dano.</p>
<p><strong>Cortejo de Lâminas</strong>: Quando equipado com uma lâmina (adaga, sabre, espada etc) e utilizando a
    destreza para atacar, o ladino pode desferir múltiplos ataques. Sempre que os dois dados de dano resultarem no mesmo
    valor, o ladino ganha um ataque adicional. Quanto menor o dado de dano, maior a chance de um novo ataque. Não existe
    limite para a sequência de ataques.</p>
<p><strong>Ladinagem</strong>: O ladino é um especialista em abrir fechaduras, furtividade, punga, falsificação, entre
    outros. Em qualquer teste relacionado a essas habilidades, ele realiza o teste com vantagem.</p>
<p><strong>Acrobacias</strong>: O ladino possui uma fluidez incrível em seus movimentos, sendo capaz de realizar longos
    saltos, andar com agilidade por telhados e cordas, mover-se furtivamente e saltar de grandes alturas sofrendo pouco
    ou nenhum dano. Em testes relacionados a essas habilidades, ele age com vantagem.</p>
<p><strong>Dança das Sombras</strong>: Em situações de baixa luminosidade (como luz de tochas ou menor), sua guarda é
    aumentada em +1 ponto.</p>
<p><strong>Esquiva Astuta</strong>: O ladino adiciona +1 ponto de proteção, independentemente do equipamento, graças à
    sua percepção aguçada em combate.</p>
<p><strong>Assassino Furtivo</strong>: Para realizar um assassinato furtivo, o alvo não deve estar ciente da presença do
    ladino ou de suas intenções. O movimento só pode ser executado durante um combate iniciado, desde que o ladino ainda
    não tenha sido visto pelos adversários. Este ataque causa 4 vezes o dano normal e deixa o oponente em estado de
    choque (sem reação) por uma rodada.</p>
<p><strong>Toximante</strong>: O ladino é um mestre no uso de venenos, recebendo vantagem em qualquer teste relacionado
    a isso, incluindo a criação de antídotos. Ele é o único capaz de aplicar venenos em suas armas de maneira eficiente,
    seja com lâminas ou flechas modificadas, tendo duas chances em 6 de o veneno fazer efeito.</p>

<h2>Alquimista</h2>

<p>O Alquimista é uma classe extraordinária em <i>Avengard</i>, destacando-se por sua habilidade única de manipular o
    éter, embora ainda não possuam o controle direto sobre ele através da força de vontade. A alquimia é uma ciência
    singular, situada entre a química e a magia, e os Alquimistas são mestres em manipular o éter de forma indireta,
    utilizando processos que lembram a química tradicional.</p>
<p>A alquimia depende da sensibilidade aos elementos alquímicos, exclusivos de <i>Avengard</i>. Essa habilidade especial
    permite aos Alquimistas criar uma vasta gama de poções, elixires e engenhocas que muitos confundiriam com
    feitiçaria, mas que, na verdade, seguem princípios científicos. Entre suas criações mais notáveis estão bombas,
    poções, apetrechos, gases paralisantes e uma infinidade de fórmulas que tornam essa classe incrivelmente versátil.
</p>
<p>Entretanto, a produção desses itens exige ingredientes raros e difíceis de obter, o que faz das expedições e da busca
    por componentes uma parte fundamental da vida dos Alquimistas. Eles estão sempre em busca de novos elementos para
    aperfeiçoar suas criações.</p>
<p>O avanço na ciência alquímica está diretamente ligado à sabedoria do Alquimista; quanto maior sua sabedoria, maior
    será seu domínio dessa arte. Devido aos custos dos estudos e dos equipamentos necessários, é incomum encontrar
    Alquimistas de origem simples, sendo comum que eles procurem mestres experientes para guiá-los.</p>
<p><strong>Equipamento Inicial</strong>: O Alquimista começa sua jornada com um kit de viagem, um destilador portátil,

<h3>Virtudes</h3>
<p><strong>Antissocial:</strong> o Bruto é introvertido e evita interações sociais. Por isso, ele tem desvantagem em
    testes de persuasão, negociação ou qualquer outro relacionado a interações sociais.</p>
<p><strong>Caminho da Força:</strong> Sempre que utilizar o atributo força para realizar um ataque, o dado de dano da
    arma deve ser dobrado, rolando-o duas vezes.</p>
<p><strong>Desconforto com Armaduras:</strong> ao usar armaduras com bônus de guarda superior a 1, o Bruto sofre
    penalidades em testes de destreza proporcionais a quanto a guarda é superior. Além disso, ele perde os benefícios do
    Caminho da Força.</p>
<p><strong>Casca Grossa:</strong> independentemente da armadura que estiver vestindo, ou não, o Bruto sempre terá pelo
    menos 4 pontos de proteção, graças à sua vida dura e sua constituição robusta.</p>
<p><strong>Finalização:</strong> o Bruto se sente revigorado após derrubar um oponente, ganhando imediatamente um novo
    turno e 1 ponto de moral.</p>
<p><strong>Deslocamento Aprimorado:</strong> ao usar armaduras com bônus de guarda de até 1, o Bruto realiza com
    vantagem testes relacionados a movimento, como correr ou se deslocar.</p>
<p><strong>Fôlego:</strong> o Bruto pode gastar 1 ponto de moral para recuperar 1d12 de vitalidade.</p>

<h2>Cavaleiro</h2>
<p>O Cavaleiro é a personificação da nobreza e honra entre as classes combatentes. Originário de famílias respeitáveis,
    ele valoriza profundamente a etiqueta refinada e os princípios éticos. Manter a honra e o renome de sua linhagem é
    sua maior prioridade, vendo o sucesso da família como o seu próprio.</p>
<p>Sua reputação é uma ferramenta valiosa, frequentemente utilizada para alcançar objetivos, especialmente quando ele
    atua como defensor de nobres ou de organizações influentes. Com armaduras elegantes e adornos imponentes, como capas
    distintivas, o Cavaleiro exibe seu status e prestígio.</p>
<p>Graças à sua origem privilegiada, o Cavaleiro começa sua jornada com mais recursos do que outras classes, incluindo
    riquezas e equipamentos de alta qualidade. Ele recebeu treinamento marcial em várias armas e aprendeu a manejar
    armaduras pesadas com grande habilidade.</p>
<p>Embora o Cavaleiro tenha uma imagem nobre, nem todos seguem essa conduta. Alguns utilizam sua fama para ganhos
    pessoais, liderando milícias ou perseguindo interesses próprios. Outros, porém, encarnam de fato os ideais de honra
    pelos quais são conhecidos. Essa diversidade de motivações provoca conflitos e rivalidades entre guildas de
    cavaleiros, que muitas vezes funcionam como poderes paralelos nas grandes cidades.</p>
<p><strong>Equipamento Inicial:</strong> Graças à sua origem abastada, o Cavaleiro inicia com uma arma e armadura de sua
    escolha, um escudo e um cavalo treinado. Ele também possui um kit nômade e 30 moedas de prata.</p>
<p><strong>Atributos:</strong> Sua nutrição rica garante +2 em Constituição. O árduo treinamento lhe confere +1 em
    Força, mas a proximidade com a corrupção das elites sociais resulta em -1 em Karma.</p>

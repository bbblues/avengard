<h1>Classes</h1>

<p>Ao criar um personagem, o jogador deverá escolher a classe à qual ele pertence. A classe funciona como um conjunto de
    definições que influenciam os atributos, itens e outras características que moldarão seu estilo de jogo. Em
    Avengard, existem nove classes disponíveis: bruto, cavaleiro, mercenário, caçador, corsário, ladino, alquimista,
    arauto e ocultista. Cada uma delas possui sua própria identidade e particularidades, que serão exploradas
    individualmente.</p>

<h2>Bruto</h2>
<p>O Bruto é um personagem que valoriza a simplicidade e enfrenta os desafios da vida com uma solução única: a força
    física. Sua abordagem é direta, sem complicações ou reflexões profundas. Com um passado geralmente distante das
    complexidades urbanas, ele não se preocupa com as formalidades da sociedade.</p>
<p>Muitos Brutos encontram trabalho em serviços braçais, guardas militares ou milícias, onde suas habilidades físicas
    são apreciadas. Eles vivem no presente, sem grandes preocupações com o futuro, o que os torna vulneráveis às
    tentações e impulsos cotidianos.</p>
<p>Seu lema poderia ser: "você só precisa do que pode carregar." Essa filosofia reflete sua forma prática e
    descomplicada de ver o mundo.</p>
<p>Os Brutos preferem armas de grande impacto, como machados de duas mãos e porretes. Apesar do peso dessas armas, eles
    as manuseiam com destreza devido à sua força física impressionante. Roupas leves são suas favoritas, já que
    priorizam a mobilidade em combate para maximizar sua agressividade e poder.</p>
<p><strong>Equipamento Inicial:</strong> o Bruto é um homem de poucas posses. Ele começa sua jornada com uma arma
    simples, como um porrete, malho ou machado de duas mãos, e veste um gibão rústico feito de peles de animais que ele
    mesmo caçou. Carrega uma mochila com kit de viajante e possui 1 moeda de prata no bolso.</p>
<p><strong>Atributos:</strong> sendo a classe mais forte, o Bruto recebe +2 em força e +1 em constituição, mas sua falta
    de astúcia resulta em -1 nesse atributo.</p>

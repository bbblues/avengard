<h3>Virtudes</h3>
<p><strong>Alfabetizado:</strong> O Cavaleiro pode ler e escrever na língua comum.</p>
<p><strong>Especialista em Armaduras:</strong> O Cavaleiro é especialista no uso de armaduras, reduzindo em 1 ponto o
    requisito de Força para usá-las e aumentando o limite de bônus de Destreza em 1.</p>
<p><strong>Juiz de Ocasião:</strong> Na ausência de uma corte, o Cavaleiro pode ser nomeado juiz de ocasião pelo
    administrador local para arbitrar disputas ou emitir sentenças.</p>
<p><strong>Trato Social:</strong> Com educação refinada, o Cavaleiro sabe se comportar e falar de forma adequada,
    especialmente entre nobres. Ele realiza testes de persuasão, comércio e situações sociais com vantagem.</p>
<p><strong>Moral Elevada:</strong> Sua moral quase inabalável concede ao Cavaleiro 16 pontos de moral no início de sua
    jornada, além de permitir que ele recupere 1 ponto de moral por dia.</p>
<p><strong>Inspirar:</strong> O Cavaleiro pode gastar 1 ponto de moral para inspirar um aliado com uma palavra de
    comando, concedendo vantagem ao próximo teste desse aliado. Esta ação pode ser realizada a qualquer momento.</p>
<p><strong>Parede de Escudos:</strong> Quando equipado com um escudo, o Cavaleiro pode proteger um aliado adjacente.
    Durante o combate, o inimigo que atacar esse aliado rola com desvantagem.</p>
<p><strong>Revidar:</strong> Se o Cavaleiro não estiver protegendo um aliado e estiver com um escudo, ele pode
    contra-atacar sempre que um ataque contra ele falhar por 5 ou mais pontos. Se o atacante estiver ao alcance, o
    Cavaleiro pode realizar um ataque em seguida respeitando as regras de engajamento.</p>
<p><strong>Proteger:</strong> O Cavaleiro pode optar por receber o dano que seria causado a um aliado que ele esteja
    protegendo com seu escudo.</p>

<h2>Mercenário</h2>
<p>O Mercenário é uma classe que emerge das turbulências da guerra, quando exércitos, desesperados por mais combatentes,
    contratam soldados para lutar por causas que muitas vezes estão distantes das preocupações cotidianas. Com o fim dos
    conflitos, muitos soldados ficam à deriva, sem direção e incertos sobre como aplicar as habilidades desenvolvidas em
    combate. Esse cenário cria uma abundância de mercenários, que vagam pelas cidades, prontos para aceitar qualquer
    trabalho, legal ou não, desde que haja pagamento.</p>
<p>É comum que mercenários busquem uma vida mais estável, aliando-se a grupos de expedicionários ligados à Guilda. No
    entanto, nem todos conseguem notoriedade suficiente para serem convidados a fazer parte dessa organização.</p>
<p>Extremamente versáteis, os Mercenários são capazes de improvisar armas e lutar desarmados quando necessário. A dureza
    da vida os tornou combatentes experientes, que exploram com astúcia os pontos fracos dos inimigos. Eles sabem como
    usar o terreno, o clima e as circunstâncias a seu favor, tornando-se mestres em táticas e estratégias não
    convencionais.</p>
<p>Além disso, Mercenários possuem uma impressionante resistência a ferimentos e venenos, e são incrivelmente capazes de
    suportar longos períodos sem água ou comida. Sobreviventes natos, estão sempre dispostos a enfrentar os desafios
    mais árduos em troca de recompensas materiais ou simplesmente pela satisfação de concluir um contrato. No mundo de
    Avengard, muitas vezes são os Mercenários que determinam o destino daqueles que recorrem a seus serviços.</p>
<p><strong>Equipamento Inicial</strong> O Mercenário, fruto de uma vida difícil, começa sua jornada com poucas posses.
    Ele inicia com um machado ou lança, armas simples e acessíveis, e uma armadura de até 2500 moedas. Além disso,
    carrega uma mochila com um kit de viajante e possui 5 moedas de prata.</p>
<p><strong>Ganho de Atributo</strong> Sua distribuição de atributos é equilibrada, ganhando +1 em força, destreza e
    constituição. No entanto, sua vida focada no combate limita sua compreensão do mundo fora da batalha, resultando em
    -1 em sabedoria.</p>
<!-- <p><img src="<?=base_url('assets/img/mercenario.png')?>" alt=""></p> -->
<h3>Virtudes</h3>
<p><strong>Fôlego:</strong> Pode gastar um ponto de moral para recuperar 1d12 de vitalidade.</p>
<p><strong>Resiliente de Guerra:</strong> Acostumado com situações extremas e escassez de recursos, o Mercenário reduz
    em 1 ponto qualquer penalidade decorrente de fome, sede ou fadiga.</p>
<p><strong>Experiência de Batalha:</strong> A cada golpe acertado, o Mercenário ignora 1 ponto da guarda do adversário
    até que ela se reduza a zero durante o combate.</p>
<p><strong>Golpe Fulminante:</strong> Se ao atacar o valor obtido no d20 for menor que a guarda do adversário, ele o
    atordoa por uma rodada.</p>
<p><strong>Trânsito de Batalha:</strong> Não sofre penalidades por engajamento, e pode combater até dois adversários sem
    ser flanqueado.</p>
<p><strong>Moral Maleável:</strong> Quando o Mercenário fizer um teste de moral, ele pode realizá-lo com vantagem.</p>
<p><strong>Finalização:</strong> Sempre que derrotar um oponente, ganha 1 ponto de moral.</p>
<p><strong>Sede de Sangue:</strong> Sempre que sofrer dano, seu próximo ataque é feito com vantagem.</p>

<h2>Caçador</h2>

<p>O Caçador é, em grande parte, um indivíduo solitário, cuja principal habilidade é a arte do rastreamento. Sua
    capacidade de observar e seguir pistas é incrivelmente refinada, tornando-o um mestre na caça. Armadilhas e armas de
    longo alcance, como arcos e bestas, são suas ferramentas preferidas, e ele é altamente especializado em preparar
    emboscadas com precisão.</p>
<p>Em Avengard, muitos Caçadores sobrevivem comercializando peles, aproveitando a fauna local para sustento. No entanto,
    alguns caçam alvos mais perigosos, como carniçais e criaturas monstruosas que assolam o mundo. Outros ainda protegem
    plantações e vilarejos contra ameaças naturais ou sobrenaturais, atuando como vigias experientes.</p>
<p>Valorizados em grupos de expedição, os Caçadores fornecem suporte essencial graças à sua proficiência em armadilhas e
    ataques à distância. Sua perícia em rastreamento é inestimável para evitar perigos, e seu vasto conhecimento em
    técnicas de sobrevivência os torna mestres em se adaptar às condições mais hostis.</p>
<p><strong>Equipamento Inicial</strong>: O Caçador começa sua jornada com 3 facas, uma adaga e uma arma de projétil à
    sua escolha. Ele também possui uma armadura com bônus de guarda inferior a 3 (2 ou menos), conforme a escolha do
    jogador. Carrega uma mochila com kit de viajante e possui 2 moedas de prata.</p>
<p><strong>Ganho de Atributo</strong>: O Caçador depende de seu conhecimento, por isso recebe +2 em sabedoria, +1 em
    força e -1 em destreza.</p>

<!-- <img src="<?=base_url('assets/img/arqueiro.png')?>" alt=""> -->

<h3>Virtudes</h3>

<p><strong>Armadilhas</strong>: O Caçador é um mestre em preparar e detectar armadilhas. Sempre que realizar um teste
    relacionado a armadilhas, ele o faz com vantagem.</p>
<p><strong>Rastreador</strong>: Como especialista em rastreamento, o Caçador realiza qualquer teste nessa área com
    vantagem.</p>
<p><strong>Sobrevivência</strong>: Acostumado a passar dias na natureza, o Caçador domina técnicas de sobrevivência,
    como cozinhar, montar acampamento, encontrar abrigo, identificar plantas comestíveis e localizar água. Ele faz
    testes de sobrevivência com vantagem.</p>
<p><strong>Camuflagem</strong>: O Caçador é habilidoso em se esconder e utiliza a camuflagem com grande eficácia. Quando
    precisa fazer um teste de camuflagem, ele o faz com vantagem. Se estiver auxiliando seus companheiros, todos também
    fazem o teste com vantagem.</p>
<p><strong>Furtivo</strong>: O Caçador realiza testes de furtividade com vantagem.</p>
<p><strong>Terreno Elevado</strong>: Quando ataca de uma posição elevada, o Caçador recebe vantagem no ataque.
    Considera-se terreno elevado qualquer ponto acima da altura do alvo.</p>
<p><strong>Instinto</strong>: O Caçador tem um senso de perigo excepcional, sempre agindo primeiro no combate.</p>
<p><strong>Perito em Arcos</strong>: Ao usar um arco de caça, o Caçador causa o dobro dos dados de dano. Usando uma
    besta, ele pode recarregar o virote sem gastar um turno.</p>
<p><strong>Tiro de Supressão</strong>: No início do combate, o Caçador pode optar por não atacar em seu turno e entrar
    em modo de vigilância, aguardando a movimentação do inimigo. Até o início do próximo turno, ele pode realizar até 3
    ataques contra inimigos que se desloquem, mas cada disparo deve ser feito com desvantagem.</p>
<p><strong>Ponto Fraco</strong>: Usando uma adaga, o Caçador pode realizar ataques nos pontos mais vulneráveis da
    armadura do inimigo, ignorando o valor de proteção.</p>

<h2>Cors&aacute;rio</h2>

<p>O Corsário é uma figura ousada e astuta, profundamente ligada ao período inicial de colonização de Avengard. Nessa
    época, estabelecer colônias no continente era extremamente arriscado, tornando as ilhas satélites refúgios cruciais.
    Essa geografia única fez da navegação uma atividade econômica essencial, mas também fomentou a pirataria nos mares
    do entorno.</p>
<p>A pirataria, em grande parte, surgiu das rivalidades entre cidades, que frequentemente contratavam corsários para
    saquear navios inimigos e proteger seus interesses. Embora muitos ainda pratiquem essas atividades, o aumento da
    vigilância pelas marinhas locais forçou muitos corsários a adaptar suas habilidades para expedições terrestres e
    novas aventuras.</p>
<p>Conhecidos por sua astúcia e persuasão, os Corsários são mestres em negociações e lábia, muitos deles também se
    destacam como trovadores e bardos. Sua habilidade na esgrima é comparável à de guerreiros treinados, tornando-os
    adversários formidáveis em combate. Além disso, seu conhecimento estratégico e mercantil os torna excelentes
    negociadores em qualquer situação.</p>
<p>Mesmo em terra firme, o coração do Corsário pertence ao mar. Seu conhecimento em navegação é vasto, sendo essencial
    para guiar expedições ou prever mudanças climáticas.</p>
<p><strong>Equipamento Inicial</strong>: O Corsário e seu sabre são inseparáveis. Estar sem ele é pior do que estar
    longe do mar. Por isso, seu primeiro equipamento será um sabre. O jogador pode escolher uma armadura com bônus de
    guarda inferior a 3 (2 ou menos). Ele carrega uma mochila com kit de viajante e possui 10 moedas de prata.</p>
<p><strong>Ganho de Atributo</strong>: O Corsário recebe +2 em astúcia, +1 em karma e -1 em constituição.</p>


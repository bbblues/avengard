<h3><strong>A Rodada de Combate</strong></h3>
<p>O combate em Avengard é dividido em rodadas. Durante a rodada, cada um dos personagens deve gastar os seus pontos de ação na ordem definida pela iniciativa. Assim que acabar esse consumo de pontos, um jogador deve passar a vez para o próximo. Os personagens que agem antes, não precisam consumir todos os seus pontos de imediato, eles podem "guardar" pontos (alguns ou todos eles) para usar depois da ação de algum outro ator. A rodada acaba quando ninguém mais quer usar os pontos, ou eles se esgotaram. Nesse momento deve-se renovar os pontos de ação de todos, e iniciar a nova rodada.</p>

<p>A fadiga pode ser consumida durante a rodada como pontos de ação, mas diferente deles, ela não se renova ao final da rodada, apenas ao final do combate. Existe o caso do personagem já ter consumido parte ou toda a sua fadiga, e finalizar a rodada sem consumir todos os seus pontos de ação. Nesse caso, ao fim da rodada, esses pontos não consumidos podem ser adicionados à fadiga, desde de que seu valor nunca ultrapasse seu valor inicial. A fadiga se recupera totalmente descansando 15 minutos após o combate.</p>
<h3><strong>Deslocamento</strong></h3>
<p>O deslocamento é a ação mais básica que pode ser feita durante o combate. para se deslocar pelos quadrantes, gasta-se 1 ponto de ação por quadrante avançado, incluindo movimentos diagonais. É possível que durante o combate o personagem resolva subir em algum lugar. Caberá ao narrador definir quantos pontos de ação gastaria-se para praticar tal ação.</p>
<h3><strong>Ataque</strong></h3>
<p>Atacar um adversário não é nada mais do que um teste da habilidade relacionada a arma equipada. A grande diferença é a dificuldade do teste, que será determinada pelo alvo atacado em seu valor de defesa. </p>

<p>Em Avengard pode-se realizar 3 tipos de ataques, cortante, perfurante ou contundente, o que define isso é a arma utilizada para realizá-lo. Algumas armas poderão realizar tanto ataques cortantes como perfurantes. Armas que possuem o dano corte ou perfuração, como as espadas, consomem [1 PA] a mais para utilizar o golpe perfurante.</p>

<p>O consumo de pontos de ação em um ataque varia com relação a arma utilizada, e na tabela de armas estará descrito o consumo representado por cada uma delas. Alguns arquétipos poderão subverter esse consumo de acordo com seus talentos.</p>
<h3><strong>Ataque Forte</strong></h3>
<p>É o ato de concentrar dois ou mais golpes em um único ataque, para garantir causar dano em adversários que possuam alta redução de dano. Um ataque de faca custa 3 pontos de ação e causa 1d6 de dano. em um turno, um personagem que poderia fazer 3 ataques com a faca consumindo 3 PA em cada ataque, pode fazer um único ataque consumindo 9PA e conferindo 3d6 de dano. Isso é um ataque forte. Este tipo de ataque pode ser vantajoso em algumas situações, mas pode ser uma desvantagem em outras.</p>
<h3><strong>Ataque Pelas Costas</strong></h3>
<p>O ataque pelas costas é um tipo de ataque específico onde o atacante tem 1d4 de bônus na jogada de ataque. Para receber esse bônus deve-se respeitar alguns critérios: primeiramente o ataque não pode ser realizado por uma arma de longa distância, têm que ser realizado com o atacante fora da área de visão do oponente e têm que ter iniciado o seu turno fora dessa área de visão.</p>
<h3><strong>Armas de Projétil</strong></h3>
<p>O ataque com arcos ou besta têm algumas diferenças do ataque tradicional. Por usarem munição, essas armas precisam ser carregadas antes de serem usadas, por esse motivo elas apresentam dois valores como consumo de pontos de ação. O primeiro valor é referente ao tempo de carga, e o segundo ao tempo de disparo. </p>
<h3><strong>Tipos de Dano</strong></h3>
<p>Existem basicamente três formas de se causar dano em um adversário: por corte, contusão ou perfuração. Em alguns casos, armas de contusão poderão ignorar parte do valor de defesa do adversário, já os perfurantes poderão ignorar parte da redução de dano.</p>
<h3><strong>Armaduras e Escudos</strong></h3>
<p>As armaduras são equipamentos que irão conferir a base do índice de defesa de um personagem. Elas são divididas em dois grupos: leves e pesadas. Cada armadura confere uma penalidade em pontos de ação quando equipadas, observando isso, qualquer personagem pode utilizar qualquer armadura. Algumas classes irão oferecer benefícios quando equipadas com tipos específicos de armaduras, é bom ficar atento.</p>

<p>O sistema de Avengard trará em sua versão mais completa regras de desgastes de itens, e isso implicará bastante na escolha das armaduras, principalmente por causa do custo de manutenção. No fastplay, essas regras não serão abordadas.</p>

<p>O uso de um escudo leve é opcional, e não confere nenhuma penalidade, apenas o bônus de +1 a sua defesa. A penalidade pelo seu uso, é a de não poder usar uma arma de duas mãos. Já o escudo pesado confere uma penalidade -1, ou seja, você perde um ponto de ação caso equipe-o, a não ser que sua classe ou talento diga o oposto.</p>
<h3><strong>Retirando os pontos de vigor</strong></h3>
<p>Toda vez que um ataque resultar em dano, esse valor deve ser subtraído do vigor de quem o recebeu, entretanto, antes o dano deverá ser subtraído da redução de dano concedida pela armadura, seguindo as regras descritas em tipos de dano.</p>

<p>Quando o vigor do personagem chegar a zero o jogador deverá fazer um teste de saúde para permanecer de pé, em caso de falha o personagem entra em colapso e desmaia, em caso de acerto ele permanece ativo mesmo com o vigor zerado. Ao receber dano novamente, um novo teste deverá ser realizado.</p>

<p>No fim do combate, o jogador que falhou em um teste de vigor, deverá fazer um teste de saúde com dificuldade 5, em caso de acerto o personagem acorda com 1 ponto de vigor. Deve-se então marcar 1 ponto de ferimento na ficha. Em caso de erro o personagem morre. Cada ponto de ferimento marcado na ficha aumentará em 1 ponto a dificuldade neste teste de sobrevivência, que inicia em 5. Esse processo se repete até que o personagem morra ou algum milagre aconteça. O narrador poderá estabelecer que ao completar um arco de uma campanha (campanha = série de aventuras) os pontos de ferimentos poderão ser zerados.</p>



<h2><strong>REGRAS COMPLEMENTARES</strong></h2>
<p>Abaixo é listada uma sequência de regras específicas para determinadas situações que ajudarão o narrador a resolver algumas situações cotidianas na aventura e complementarão algumas mecânicas de jogo.</p>
<h3><strong>Descansando</strong></h3>
<p>Descansar é fundamental para recuperar-se de um dia desgastante, e para isso um personagem precisa de pelo menos 8 horas de sono. Ao se dormir menos de 8h, o jogador precisará fazer um teste de saúde, com penalidade equivalente ao número de horas faltantes. Em caso de acerto, será como se tivesse plenamente descansado, e em caso de erro, ficará com -1 de penalidade em todos os testes até que consiga fazer um repouso pleno. Sempre que completar 8 horas de sono o personagem recupera todo o seu vigor.</p>
<h3><strong>Compartilhando a Narrativa</strong></h3>
<p>Em Avengard RPG, cabe aos jogadores definirem apenas as ações de seus personagens, os protagonistas, e ao narrador cabe descrever a ambientação e todos os outros personagens, entretanto, existe uma exceção. Todo jogador pode impor uma narração a qualquer momento usando um ponto de éter.</p>

<p>A jogada funciona da seguinte forma, o jogador sinaliza ao narrador que pretende fazer uma intervenção narrativa, e descreve, como se fosse o narrador da aventura, uma pequena frase. O narrador deverá avaliar o quão provável aquilo é, e definir um bônus ou uma penalidade. O jogador fará um teste de sorte, usando a penalidade aplicada, e em caso de acerto, a narração é incorporada na aventura. Existe a possibilidade de o narrador considerar a narração óbvia ou impossível, caso seja óbvia, nenhum teste precisará ser feito, e caso impossível, o narrador recusa a narrativa, e devolve o ponto de éter ao protagonista.</p>

<p>Imagine que um grupo de aventureiros precisa entrar em uma caverna para verificar algo, entretanto, nenhum deles possui tocha ou algo que possa iluminar a escuridão. Um dos jogadores resolve testar a sorte, e narra que um amigo seu, no vilarejo anterior, colocou uma tocha em sua mochila. O narrador do jogo deve negar qualquer narração retroativa a momentos já jogados pelo grupo, por isso, dá como impossível. Um outro jogador então também deseja testar a sorte, e narra que ao chegarem na caverna, encontram alguns equipamentos de exploração jogados na entrada. O narrador precisa avaliar se existe algum motivo para isso acontecer (ter uma vila perto já seria um bom motivo), se existe um, pode-se dar um bônus para esse teste, se apenas não existe motivo, pode-se pedir um teste simples, ou se existe um motivo para não ter esse equipamento ali, pode-se definir uma penalidade. O bônus e a penalidade podem variar de 1 a 5, a critério do narrador.</p>
<h3><strong>Atordoamento</strong></h3>
<p>Durante uma aventura, alguns eventos podem acontecer com a consequência de deixar um ou mais personagens atordoados, seja de uma simples pancada na cabeça a uma explosão. Um personagem atordoado perde a resposta aos 5 sentidos do corpo, ou seja, deve ser considerado cego, surdo, mudoe incapaz de sentir cheiro ou sabor, enquanto durar o atordoamento.</p>
<h3><strong>Reputação</strong></h3>
<p>Toda ação tomada pelos personagens dos protagonistas poderá gerar uma reputação para eles, mas diferente do que dizem por aí, que reputação é algo que se constrói, na prática, a coisa é bem mais efêmera. A última ação de conhecimento público que o personagem tomar, será a sua reputação atual. Uma nova reputação apaga a anterior, pois a memória do povo é curta.</p>

<p>Supondo que o grupo ajudou um camponês a encontrar seu irmão perdido, sem cobrar nada por isso, uma reputação de benfeitor seria adequada, caso cobrassem, talvez o título de mercenários confiáveis fosse mais adequado. Suponhamos que o grupo sequestrou o irmão por dinheiro, isso poderia gerar a reputação de bandidos ou sequestradores. O narrador pode acumular reputações diferentes, que interferem em diferentes casos.</p>

<p>Possuir a reputação não significa sofrer os efeitos dela, para que os efeitos sejam percebidos, a fama tem de chegar aos ouvidos. Para isso, o narrador poderá pedir um teste de sorte e definir se a fama chegou. Para definir um bônus ou penalidade para esse teste, deve-se avaliar o quão possível é da fama ter chegado até a pessoa abordada no momento. Se a fama é ruim, um erro no teste indica que sim, se a fama for boa, um acerto.</p>
<h3><strong>Teste de Sorte Coletivo</strong></h3>
<p>Muitas vezes o narrador precisará testar a sorte do grupo, e não de um indivíduo. Esse caso é simples, deve-se pegar o menor dado que tenha mais faces que o número de jogadores. Cada jogador escolhe um número, e joga-se esse dado até que se obtenha o número escolhido. Esse personagem deverá fazer o teste de sorte para o grupo.</p>
<h3><strong>Morte de Personagens do Narrador</strong></h3>
<p>Quando se trata de personagens sem relevância na narrativa, o narrador pode considerar morte assim que o vigor chegar a zero. Quando a relevância existe, ele pode considerar a mesma regra utilizada para os personagens dos jogadores.</p>
<h2><strong>ITENS E SUPRIMENTOS</strong></h2>
<p>Cada classe irá definir a quantidade de itens e dinheiro que o personagem irá iniciar a aventura neste fastplay. Avengard possui um sistema monetário simples, baseado em cunhagem de moedas metálicas escalonadas em um arranjo decimal. As moedas são cunhadas dentro de cada reino ou estado, e consequentemente carregam seu brasão. Uma moeda de um reino tem o mesmo valor em reinos vizinhos e por esse motivo os jogadores não precisarão se preocupar com o câmbio. Uma moeda de ouro tem o valor de dez moedas de prata, ou de cem moedas de cobre. Todos os itens abaixo têm o seu valor informado em moedas de cobre.</p>

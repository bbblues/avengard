<h1>Fastplay Avengard Alfa 1.1_</h1>
<p>Esta é a versão alfa 1.1 de testes de mecânicas do RPG Avengard utilizando um sistema baseado em testes com o dado de 12 faces, e personagens montados em cima de classes, ainda não contemplando regras evolutivas.</p>
<p>Em comparação a versão anterior, esta reformulou as classes, transferiu para a classe a responsabilidade pelo cálculo do vigor, separou as habilidades de combate das demais habilidades, e desindexou elas da destreza e indexou a orientações da classe e reformulou a tabela de armaduras dividindo apenas entre leves e pesadas.<br/><br/></p>
<p>Este sistema usará cinco tipos de dados diferentes, com quatro, seis, oito, dez e doze faces, que serão chamados respectivamente no decorrer deste texto de d4, d6, d8, d10 e d12.</p>
<p><a href="https://avengard.com.br/assets/ficha-avengard-alfa.pdf">Clique aqui</a> para fazer o download do arquivo para imprimir a ficha de personagem</p>
<p>Para montar um personagem para o fastplay, aconselha-se a primeiramente definir uma classe, e após isso os atributos. Os atributos definirão quantas habilidades e de que tipo o seu personagem terá. Por fim, escolha seus itens iniciais.</p>
<h2><strong>DEFININDO ATRIBUTOS</strong></h2>
<p>Um personagem em Avengard RPG possui seis atributos principais: <strong>força, destreza, astúcia, conhecimento, saúde e sorte.</strong> Eles definirão as características gerais e terão valores que irão variar entre zero e três. Um valor zero em força representaria um personagem franzino, incapaz de usar armas mais pesadas, já um valor três, representaria algo como um halterofilista.</p>
<p>Todo jogador recebe dez (10) pontos para distribuir em seus atributos respeitando uma regra, nenhum atributo pode ter valor superior a três.</p>
<p>A força define o tamanho da força física, o quanto se pode carregar, armas que se pode usar e pode impactar no dano que o personagem causa. A destreza define a coordenação motora e o quanto as habilidades motoras poderão evoluir, o que interfere diretamente na chance de acerto delas. A astúcia representa a inteligência do personagem, e define a quantidade e evolução máxima das habilidades intelectuais. O conhecimento tem seu nome autoexplicativo, define o quanto o personagem tem de conhecimento sobre as coisas. Define a quantidade e evolução máxima das habilidades cognitivas. A saúde representa tanto a saúde física quanto mental do personagem. Ela influencia nos pontos de vigor, resistência física e resiliência. Uma boa saúde aumenta consideravelmente as chances de uma vida mais longa. Por fim, a sorte do personagem determinará o quanto o universo está a seu favor. Ela é utilizada para determinados testes que serão explicados mais à frente.</p>
<h2><strong>INDICADORES</strong></h2>
<p>Os indicadores são uma série de valores que demonstram em números alguns status do personagem.</p>
<p><strong>Vigor:</strong> indica o quanto o personagem suporta lesões antes que elas se tornem um ferimento sério. Cada classe tem sua própria regra de cálculo de vigor, que irá aumentar conforme o personagem aumentar de nível.</p>
<p><strong>Ferimentos:</strong> revela o quanto o seu personagem está realmente ferido. Quanto mais ferido ele estiver, maior a probabilidade de morte. Esse indicador inicia vazio, ou seja, sem ferimentos.</p>
<p><strong>Defesa:</strong> é o indicativo de proteção de seu personagem, quanto mais alta é sua defesa, mais difícil será de lhe acertarem golpes. Armaduras, itens e talentos podem ampliar este valor.</p>
<p><strong>Redução de Dano (RD):</strong> é uma propriedade concedida pela armadura equipada, talentos, itens etc. Todo dano sofrido deverá ser reduzido deste indicador, salvo quando informado o contrário.</p>
<p><strong>Carga:</strong> representa o quanto o personagem consegue carregar ou levantar. O valor de carga é definido por 5 + (força x 5). Este é o valor em kg que um personagem pode carregar sem que sofra nenhum tipo de penalidade. Superando o valor de carga, o personagem recebe 1 de penalidade. Superando o dobro, recebe 2, indo no máximo até 5, pois não poderá carregar mais do que isso. Cada ponto de penalidade reduz um ponto de ação.</p>
<p><strong>Indicadores de Éter:</strong> devem iniciar cheios, eles servirão para os personagens realizarem movimentos especiais, fazer uma interferência narrativa ou usar itens mágicos.</p>
<p><strong>Fome e Sede:</strong> devem iniciar vazios em toda ficha. Conforme o personagem for ficando desidratado e desnutrido, esses indicadores serão preenchidos. O fastplay não utilizará esses indicadores.</p>
<p><strong>Pontos de Ação (PA):</strong> representam o quanto o personagem pode agir em uma rodada de combate. A quantidade inicial de pontos de ação de cada personagem é dez.</p>
<p><strong>Fadiga:</strong> funciona de forma semelhante aos pontos de ação com a diferença de que eles não se renovam a cada rodada, mas sim descansando 15 minutos após o combate. Os pontos de fadiga podem ser consumidos para complementar pontos de ação faltantes em uma ação desejada, e podem ser gastos total ou parcialmente conforme escolha do jogador. A fadiga inicial é duas vezes a saúde +6.</p>
<p>O espaço para modificadores temporários é deixado para que o jogador possa ir anotando estes quando os receber durante a partida. Eles poderão ser tanto positivos quanto negativos.</p>
<h2><strong>CLASSES</strong></h2>
<p>Ao criar um personagem, o jogador deverá escolher uma classe para ele. A classe de personagem é um conjunto de benefícios e talentos predefinidos que definirão a essência do personagem. Os benefícios são passivos, eles melhoram o personagem independente da ação do jogador, já os talentos são habilidades especiais que o jogador deverá optar por ativá-las durante o jogo, a um custo descrito em cada uma delas.</p>
<p>O fastplay de Avengard contempla as quatro classes iniciais do jogo, o bruto, aventureiro, patife e místico. Neste jogo, os personagens evoluem, e essa evolução é medida em níveis. Um novo personagem deve iniciar em uma classe iniciante com o nível 1, e nela poderá evoluir até o nível 5. Quando obtiver prontos para atingir o nível 6, o jogador deverá escolher uma das especializações que se ramificam de sua classe inicial. O fastplay não abordará as especializações.</p>
<p>Avengard é um cenário de fantasia medieval sombria, isso significa dizer, que seu personagem não é um herói com talentos que o destacam dos comuns, na verdade ele é um comum, que poderá ter algum destaque mais por suas conquistas do que por seu poder de fato. Esteja preparado para um início modesto e um percurso dotado de muita cautela, pois sem ela, dificilmente atinge-se os níveis mais altos.</p>


<h2><strong>HABILIDADES</strong></h2>
<p>Habilidades são competências que atribuímos a nossos personagens, e cada uma delas está indexada a um atributo, destreza, astúcia e conhecimento. Quanto maior o atributo, mais habilidades relacionadas a ele o personagem terá, e mais evoluídas elas serão. As habilidades de combate pertencem a um grupo separado, e são definidas pela classe de personagem.</p>

<p>Existem três atributos que irão adicionar habilidades ao personagem, a destreza, a astúcia e o conhecimento. Um atributo nível zero, concede uma habilidade indexada a ele em nível básico (zero). Um atributo nível 1 concede uma em nível básico e outra nível 1. Um atributo nível 2 concede 3 habilidades nos níveis básico, 1 e 2. Por fim, seguindo a ordem, um atributo nível 3 concede 4 habilidades, uma em básico, outra em 1, outra em 2 e mais uma em 3.</p>
<h3><strong>Testando Uma Habilidade</strong></h3>
<p>Após anotar corretamente suas habilidades em sua ficha de personagem, o jogador poderá usá-las durante as aventuras. Para fazer um teste de habilidade, o jogador diz qual habilidade irá usar e lança um D12, o valor obtido no dado deve ser somado ao nível da habilidade. Caso o resultado seja maior ou igual a dificuldade estipulada para ele, significa que teve-se sucesso no teste, caso o valor resultante seja menor, o personagem errou o que tentou fazer.</p>

<p>É importante lembrar, que em todo teste de habilidade, sempre que o dado apresentar 1, é um erro, e sempre que apresentar 12, é um acerto, independente do resultado final da soma.</p>

<p>Pode ser que o jogador precise que seu personagem faça uma ação, que resulte em um teste de uma habilidade que ele não possui. Nesse caso o nível da habilidade deve ser considerado como -3. </p>
<h3><strong>Definindo Facilidades e Penalidades</strong></h3>
<p>Um teste de habilidade padrão, têm dificuldade 7, o que significa que se o resultado da soma do d12 com o bônus da habilidade testada for igual ou superior a 7, o teste foi um acerto. Entretanto, alguns testes poderão ser considerados mais fáceis ou difíceis do que o normal e caberá ao narrador definir a dificuldade do teste.</p>

<p>Um teste considerado fácil teria uma dificuldade 4, um médio dificuldade 7, um difícil teria dificuldade 10 e um muito difícil teria dificuldade 13. O narrador pode escolher qualquer valor entre 4 e 13 para ditar a dificuldade de um teste.</p>

<p>A dificuldade de um teste de habilidade pode variar de forma mais material, como ao tentar caminhar furtivamente sobre um chão pedregoso, saltar sobre um vão relativamente maior do que o normal etc. Também pode ter um caráter mais subjetivo, e nesse caso, ela poderá depender da descrição da ação do jogador. Dizer que vai procurar por armadilhas em um determinado cômodo, deve ser mais difícil do que dizer que vai procurar armadilhas na porta do cômodo. Quanto mais específico é o detalhamento da ação, mais fácil deve ser o teste. O mesmo para tentar convencer um NPC de algo, quanto melhor ou pior o argumento, mais fácil ou difícil o teste.</p>
<h3><strong>Fator de Acerto (fa)</strong></h3>
<p>Ao realizar um teste de habilidade de dificuldade 7, por exemplo, uma jogada que resulte em 10 será um acerto, assim como uma jogada que resulte em 13. É importante perceber que quem obteve 10, acertou por 3 pontos a mais do que se precisava, e quem obteve 13, acertou por 6 pontos a mais. Esse é o Fator de Acerto (fa), a diferença entre o valor resultante da jogada e a dificuldade imposta pelo narrador.</p>

<p>O Fator de Acerto pode ser positivo, como nos exemplos acima, pode ser negativo, caso o valor resultante da jogada seja menor do que a dificuldade imposta pelo narrador, e pode ser zero, caso a jogada resulte exatamente no valor da dificuldade. Este valor pode ser utilizado pelo narrador, para entender o quão bom foi um acerto ou quão ruim foi um erro.</p>
<h3><strong>Habilidades de Combate</strong></h3>
<p>As habilidades relativas ao combate não são indexadas a nenhum atributo, mas sim a classe de personagem. Cada classe irá definir quais e quantas dessas habilidades o jogador poderá ter. Lá também será determinado o nível de cada uma.</p>

<p><strong>Armas de Haste:</strong> Contempla o uso de toda arma que possua uma haste longa, como a lança arpão. Este tipo de arma pode ser usada para ataques mais distantes que as demais, tendo 1 quadrado (1 metro) a mais de alcance. Estas armas são muito eficientes em infantarias militares, entretanto, em combates mais desorganizados, elas são mais lentas para se mudar a direção de ataque (com exceção do bastão).</p>

<p><strong>Armas de Impacto:</strong> Este grupo inclui o tacape, maça, martelo, mangual e malho.</p>

<p><strong>Armas de projétil:</strong> Contempla toda arma que necessite de munição, como a besta, o arco, a zarabatana e etc. A zarabatana não detém a capacidade de causar dano por si só, ela geralmente é utilizada para a aplicação de venenos a distância. </p>

<p><strong>Arremesso:</strong> É a habilidade de arremessar armas e objetos. Contempla o arremesso de dardos, machadinhas, arpões, pedras, facas, adagas e etc.</p>

<p><strong>Combate Desarmado:</strong> Serve para se combater usando o próprio corpo como arma ou acessórios como garras e soqueiras.</p>

<p><strong>Espadas:</strong> Habilidade que norteia o uso de todo tipo de espada, como o sabre, espada curta, a bastarda e montante.</p>

<p><strong>Lâminas Curtas:</strong> Contempla as armas com lâminas de no máximo 30 centímetros, como facas e adagas. A habilidade contempla tanto o uso da arma arremessando-a como no uso em mãos, em combate corpo-a-corpo.</p>

<p><strong>Machados:</strong> Contempla o uso de todo tipo de machado no combate corpo-a-corpo.</p>
<h3><strong>Habilidades</strong></h3>
<p>Abaixo segue a lista de habilidades separada pelo atributo indexador, na ordem habilidades motoras, habilidades intelectuais e habilidades cognitivas. As motoras são referentes a destreza do personagem, dependem de uma boa coordenação motora. As intelectuais dependem da astúcia, refletem diretamente a capacidade de raciocínio e por fim as cognitivas, que são habilidades que dependem de conhecimento.</p>

<p>HABILIDADES MOTORAS</p>

<p><strong>Acrobacias:</strong> Saltar, equilibrar e se esquivar são ações que pedirão um teste de acrobacias. Essa habilidade refere-se aos testes de movimentação corporal que exijam coordenação motora e equilíbrio. Um salto complicado, uma pirueta durante o combate, escalar uma parede e etc. É sempre bom ter acrobacias como habilidade.</p>

<p><strong>Escalar:</strong> Trata-se de saber escalar superfícies difíceis com equipamentos necessários para realizar a tarefa. Cada situação demandará certos equipamentos. Escaladas que podem ser feitas sem equipamentos devem usar a habilidade acrobacias. Um escalador profissional confere nível básico nesta habilidade para qualquer pessoa que escale junto dele.</p>

<p><strong>Furtividade:</strong> é a capacidade do personagem de caminhar sem fazer barulho. A armadura equipada, os itens que se esta carregando e o tipo de solo deverão influenciar na dificuldade deste teste.</p>

<p><strong>Furto:</strong> pegar ou colocar algo no bolso de alguém sem ser percebido exigirá a habilidade furto.</p>

<p><strong>Prestidigitação:</strong> é a capacidade de realizar movimentos extremamente coordenados com as mãos. Truques de mágica com moedas, cartas e etc.</p>

<p>HABILIDADES INTELECTUAIS</p>

<p><strong>Armadilhas:</strong> Esta habilidade contempla tanto a capacidade de se fazer armadilhas complexas, como de detectar armadilhas em um ambiente.</p>

<p><strong>Comoção:</strong> sempre que se desejar convercer alguém sobre alguma coisa, a habilidade utilizada é a comoção. Comover inclui atos como passar a lábia em alguém, ser dissimulado, seduzir e qualquer outra atividade relacionada. </p>

<p><strong>Disfarce:</strong> habilidade de se disfarçar permite ao personagem a ocultação de sua real identidade em público, a mimetização de seu estado emocional e a capacidade de se camuflar tornando-se perceptível aos desatentos. O disfarce não permite se passar por alguém que o alvo do ludíbrio conheça. </p>

<p><strong>Investigação:</strong> sempre que o personagem procurar por uma passagem secreta, um item escondido ou tentar entender a cena de um crime, ele deverá usar a habilidade de investigação. Esta habilidade também pode ser usada para ocultar pistas.</p>

<p><strong>Jogo:</strong> Utilizada quando o personagem participa de qualquer jogo que exija de seu intelecto.</p>

<p>HABILIDADES COGNITIVAS</p>

<p><strong>Acampamento:</strong> Habilidade para saber montar um acampamento ou local improvisado para passar a noite. Quanto menos equipamentos e materiais adequados para o fazer, mais difícil será. Inclui a capacidade de fazer uma fogueira usando pederneiras. O personagem pode fazer um acampamento completamente rústico, apenas com materiais coletados no local, mas isso deverá ter uma dificuldade mais elevada, e o local deverá conter os materiais adequados. </p>

<p><strong>Alfaiataria:</strong> Irá lidar com tudo que envolve couro e tecido. Saberá trabalhar desde o processo de extração destes materiais até sua aplicação. Poderá fazer roupas e sapatos, armaduras, equipamentos para montaria e semelhantes.</p>

<p><strong>Arqueologia:</strong> Ciência que estuda costumes, cultura e tecnologia dos antigos habitantes de Avengard, através de fósseis, artefatos, monumentos etc. que restaram deles.</p>

<p><strong>Arrombamento:</strong> Serve para abrir fechaduras, arrombar portas ou janelas, abrir grilhões e etc. O ato pode requerer ferramentas específicas.</p>

<p><strong>Arte:</strong> Compreende toda a gama artística como a música, pintura, escultura, poesia etc.</p>

<p><strong>Carpintaria:</strong> É a habilidade de preparar a madeira e fazer objetos com ela. O carpinteiro pode trabalhar fazendo armas, escudos, móveis, papel e tudo que envolva o uso de madeira, como armas de cerco a castelos.</p>

<p><strong>Culinária:</strong> Utilizada para fazer qualquer tipo de preparo alimentício. </p>

<p><strong>Fauna:</strong> Trata do personagem conhecer o mundo animal. Pode-se conhecer os costumes e habitat de determinada espécie, formas de se proteger, domesticar e adestrar.</p>

<p><strong>Heráldica:</strong> conhecimento sobre símbolos da sociedade, brasões e estilos. Pode-se identificar a que cultura um item provém.</p>

<p><strong>Herbalismo:</strong> habilidade que consiste no domínio do conhecimento da natureza. Saber o que se pode ou não comer e seus efeitos, identificar determinadas plantas ou espécies de animais e suas peculiaridades.</p>

<p><strong>Metalurgia:</strong> é a habilidade de fazer e moldar ferro. Com o equipamento adequado, o ferreiro pode extrair o metal da natureza e fazer ou consertar objetos de ferro, como armas, armaduras e utensílios domésticos.</p>

<p><strong>Rastreamento:</strong> Consiste na capacidade de se rastrear um animal ou pessoa pelos rastros deixados pelo caminho.</p>
<h2><strong>COMBATE</strong></h2>
<p>Muitas das regras de combate dependerão de distância e disposição dos atores no campo de batalha. Para que todos tenham visibilidade sobre a situação, aconselha-se o uso de um tabuleiro quadriculado, onde peças podem representar os envolvidos. Cada quadrado representa a área de 1,5 metro, e só pode ser ocupado por um personagem. O tamanho mais utilizado é com quadrados de 2,5 cm de lado para peças de até 3 cm de altura. Aconselha-se um tabuleiro que tenha no mínimo 20x30 quadrados.</p>
<h3><strong>Definindo a iniciativa</strong></h3>
<p>Antes de se iniciar o combate é importante definir a ordem em que os envolvidos irão atuar, este teste é chamado de teste de iniciativa, e ele pode ocorrer de duas maneiras: surpresa ou declarada.</p>

<p>Quando uma das partes surpreende a outra pegando-a desprevenida, entende-se que o grupo foi surpreendido, e nesse caso, o lado que surpreendeu tem a iniciativa do combate. Somente quando todos os personagens do lado que surpreendeu tiverem executado suas ações é que o lado surpreendido poderá executá-las. A ordem de qual personagem de cada lado agirá primeiro pode ser definida pelo grupo.</p>

<p>A outra situação de combate é quando ela acontece de forma declarada, e as duas partes estão cientes do resultado inevitável. Nesse caso deve-se fazer um teste de iniciativa. O teste de iniciativa nada mais é do que um teste individual de astúcia. Os personagens que obtiverem maior valor no teste agem primeiro.</p>
<h3><strong>Campo de Visão</strong></h3>
<p>A frente de um personagem sempre deve estar apontando para um dos lados ou cantos do quadrado do qual ele se encontra, e sua área de visão é sempre de 180°. Considerando o ângulo, todo o restante deve ser considerado de costas. Na ilustração, a área verde representa o campo de visão, e a vermelha representa as costas.</p>

<h2>Equipamentos</h2>

<p>Os equipamentos representam elementos essenciais que desempenham um papel importante nas resoluções de narrativas e escolhas dos personagens. Este capítulo se dedica à análise dos equipamentos, que se subdividem em quatro categorias distintas: itens de necessidade básica, de taverna, armas e armaduras. </p>

<h3>Sistema monetário de Avengard</h3>

<p>Dentro do continente de Avengard, um elaborado sistema monetário desempenha uma função essencial no comércio, na obtenção de bens e na economia das cidades. Esse sistema é fundamentado em quatro unidades de moeda: cobre, prata, ouro e thalânio, sendo que a cada dez moedas da unidade anterior é equivalente a uma da subsequente, na ordem mencionada.</p>

<img src="<?=base_url('assets/img/mercado.png')?>" alt="">

<p>Além das moedas tradicionais, a população de Avengard também reconhece o valor das pedras preciosas como uma forma de moeda ainda mais valiosa. Diamantes, esmeraldas, safiras e rubis são frequentemente utilizados em transações de alto valor, sendo apreciados por sua raridade e beleza. Essas pedras preciosas são cuidadosamente avaliadas por ourives experientes e comerciantes especializados, desempenhando um papel crucial nas trocas comerciais e no acúmulo de riqueza no continente de Avengard.</p>

<p>Carregar moedas e pedras preciosas pode tornar-se uma carga pesada, no entanto, alguém que venha a se preocupar com esse "fardo" terá os meios para suportá-lo. Qualquer personagem pode transportar até 300 moedas sem que o peso seja uma preocupação, sendo esse peso insignificante. No entanto, a partir desse limite, passa a ser considerado um peso de 1 kg para cada 300 moedas. O cálculo do peso das moedas não se aplica àqueles que pretendem transportar dinheiro para uso diário, mas sim àqueles que estão pilhando algum tesouro.</p>

